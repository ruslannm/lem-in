#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	size_t i;

	if (s == NULL || fd == 0)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}
