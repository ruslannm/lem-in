#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			return (((void*)s) + i);
		i++;
	}
	if ((char)c == '\0')
		return (((void*)s) + i);
	return (NULL);
}
